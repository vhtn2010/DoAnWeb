function padNumber(value) {
  return String(value).padStart(2, '0')
}

function slugifyValue(value = '') {
  return String(value)
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

function createSeatOption({
  id,
  name,
  badge = '',
  price = 0,
  seat_type = '',
  benefits = [],
  is_default = false,
  is_primary = false,
}) {
  return {
    id,
    name,
    badge,
    price,
    seat_type,
    benefits,
    is_default,
    is_primary,
  }
}

function createSeat({
  carId,
  number,
  price,
  status = 'available',
  codePrefix = '',
}) {
  return {
    id: `${carId}-seat-${padNumber(number)}`,
    code: `${codePrefix}${padNumber(number)}`,
    number: padNumber(number),
    status,
    price,
    car_id: carId,
  }
}

function createCar({
  id,
  name,
  label,
  tab_label = '',
  seat_type,
  total_seats,
  price,
  booked_seat_numbers = [],
  code_prefix,
  row_size = 4,
  aisle_after = 2,
  group_size = 4,
  group_columns = 2,
}) {
  const bookedSeatSet = new Set(booked_seat_numbers)

  return {
    id,
    name,
    label,
    tab_label,
    seat_type,
    total_seats,
    layout: {
      row_size,
      aisle_after,
      group_size,
      group_columns,
    },
    seats: Array.from({ length: total_seats }, (_, index) =>
      createSeat({
        carId: id,
        number: index + 1,
        price,
        status: bookedSeatSet.has(index + 1) ? 'booked' : 'available',
        codePrefix: code_prefix,
      }),
    ),
  }
}

